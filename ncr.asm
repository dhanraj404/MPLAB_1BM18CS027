.MODEL SMALL
.DATA
N EQU 05H
R EQU 02H
RES DW ?
.CODE
MOV AX,N
MOV BX,R
mov res,0000
CALL NCR
mov bx,res
mov ah,4ch
int 21h
NCR PROC NEAR
CMP AX,BX
JE RES1
CMP BX,0
JE RES1
CMP BX,1
JE RESN
DEC AX
CMP BX,AX
JE INCR
PUSH AX
PUSH BX
CALL NCR
POP BX
POP AX
DEC BX
PUSH AX
PUSH BX
CALL NCR
POP BX
POP AX
RET
RES1:INC RES
     RET
INCR:INC RES
RESN:ADD RES,AX
     RET
     NCR ENDP
     END

